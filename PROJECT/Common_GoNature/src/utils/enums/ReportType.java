package utils.enums;

/**
 * The ReportType enum represents different types of reports.
 */
public enum ReportType {
	/**
	 * Represents an unsupported report type.
	 */
	Unsupported,
	/**
	 * Represents a total visitors report.
	 */
	TotalVisitorsReport,
	/**
	 * Represents a usage report.
	 */
	UsageReport,
	/**
	 * Represents a visits report.
	 */
	VisitsReports,
	/**
	 * Represents a cancellations report.
	 */
	CancellationsReport
}